                <ul>
                  <li><strong>Poor diet:</strong> Eating saturated fat, found in animal products, and trans fats, found in some commercially baked cookies and crackers and microwave popcorn, can raise your cholesterol level. Foods that are high in cholesterol, such as red meat and full-fat dairy products, will also increase your cholesterol.</li>
                  <li><strong>Obesity:</strong> Having a body mass index (BMI) of 30 or greater puts you at risk of high cholesterol.</li>
                  <li><strong>Lack of exercise:</strong> Exercise helps boost your body's HDL, or "good," cholesterol while increasing the size of the particles that make up your LDL, or "bad," cholesterol, which makes it less harmful.</li>
                  <li><strong>Smoking:</strong> Cigarette smoking damages the walls of your blood vessels, making them more prone to accumulate fatty deposits. Smoking might also lower your level of HDL, or "good," cholesterol.</li>
                  <li><strong>Age:</strong> Because your body's chemistry changes as you age, your risk of high cholesterol climbs. For instance, as you age, your liver becomes less able to remove LDL cholesterol.</li>
                  <li><strong>Diabetes:</strong> High blood sugar contributes to higher levels of a dangerous cholesterol called very-low-density lipoprotein (VLDL) and lower HDL cholesterol. High blood sugar also damages the lining of your arteries.    </li>
                </ul>
